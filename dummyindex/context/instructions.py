# --- HOW_TO_USE.md -----------------------------------------------------------


_HOW_TO_USE = """\
# How to use `.context/`

This folder is the project's **canonical machine-readable context** for AI coding agents (Claude Code, Cursor, Codex, Aider). It is generated by dummyIndex and refreshed by `dummyindex context rebuild --changed`.

## Read this folder *first* for any non-trivial work

Before grepping, reading files, or asking the user for context, consult these in order:

| Question | Look at |
|---|---|
| "What is this project?" | `PROJECT.md` |
| "What's the layout?" | `architecture/overview.md`, then `tree.json` |
| "How does feature `Z` work?" / "What's the flow when…?" | **Start at `features/INDEX.json`** — then `features/<feature-id>/feature.json` + `features/<feature-id>/README.md`. Drill into `architecture.md` / `implementation.md` / `data-model.md` / `security.md` / `product.md` for the relevant domain. See `features/HOW_TO_NAVIGATE.md`. |
| "Where is `X` defined?" | `map/symbols.json` — every class / function / method has `path` + `range` |
| "What files contain `Y`?" | `map/files.json` — full file inventory with language + size |
| "How does `X` relate to `Y`?" | `features/symbol-graph.json` — NetworkX graph with `contains`, `method`, `inherits`, `imports`, `calls` edges + Leiden communities |
| "Communities / god-nodes / hidden dependencies?" | `features/COMMUNITIES.md` |
| "What's the naming style?" | `conventions/naming.md` — honor this when adding new code |
| "How is code grouped into folders?" | `conventions/folder-organization.md` |
| "What patterns / idioms does this repo use?" | `conventions/coding-practices.md` |
| "How are tests written here?" | `conventions/testing.md` |
| "How does this repo talk to its database?" | `conventions/data-access.md` |
| "How do I add / change / fix X?" | `playbooks/` — task-specific recipes |

## Retrieval principle: walk the tree, don't grep

This index uses **PageIndex-style reasoning-based retrieval**. The procedure for any non-trivial question:

1. Read `features/INDEX.json` (the table of contents).
2. Reason over the names + summaries. Pick 1–3 features the task touches.
3. Read the chosen `feature.json` + `README.md`.
4. Drill into the domain section (`architecture.md` / `implementation.md` / `data-model.md` / `security.md` / `product.md`).
5. Follow `path:range` citations to source when you need to verify.

**Never grep the source tree first.** The tree has already been mapped; reasoning over the map beats string-matching the source.

## Concrete examples

**Asked to add a new feature?**
Read `playbooks/add-feature.md`. It walks through: check for duplicates in `map/symbols.json`, mirror an existing feature's structure (find via `tree.json`), honor `conventions/naming.md`, write tests first.

**Asked to find or modify something specific?**
Don't grep blindly. `map/symbols.json` has every symbol's `path:range`; load it once, find the match, jump straight to the code.

**Asked an architectural question?**
Start with `PROJECT.md` + `architecture/overview.md`. Most questions answer themselves at that level. Drill into `tree.json` only if you need finer detail.

**Doing a rename or refactor?**
`map/symbols.json` gives you every site to consider. Combine with conventional grep for callers.

## When the index is wrong

The code is the source of truth. If the index disagrees with what you see in a file, trust the file and re-run:

```
dummyindex context rebuild --changed
```

That re-hashes every source file and rebuilds only the changed bits.

## Feature-oriented navigation

`features/` is the **behavioral** view of the codebase (vs. `tree.json` which is the structural view). Start with `features/INDEX.json` when the user asks "how does X work?" or "what's the flow when…?" — it points at per-feature `feature.json` + `flows/<flow-id>.json` so you can answer without grepping. Folder names get renamed during enrichment but `feature_id` is stable, so always navigate by ID.

## Conventions for agents

- **Don't write to `.context/` by hand.** All files are regenerated on rebuild.
- **`cache/` is per-machine** — gitignored. Never reference it.
- **Always check `conventions/naming.md`** before naming a new symbol or file.
"""


def generate_how_to_use_md() -> str:
    return _HOW_TO_USE


