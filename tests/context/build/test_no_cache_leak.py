"""Verify dummyindex v0 keeps all generated artifacts inside .context/."""

from __future__ import annotations

import os
import shutil
from pathlib import Path

import pytest

from dummyindex.context.build.runner import (
    _MANAGED_GITIGNORE_PATTERNS,
    build_all,
    ensure_context_gitignore,
)
from tests.paths import SAMPLE_REPO

_FIXTURE_ROOT = SAMPLE_REPO


@pytest.fixture
def sample_repo(tmp_path: Path) -> Path:
    dest = tmp_path / "sample_repo"
    shutil.copytree(_FIXTURE_ROOT, dest)
    return dest


@pytest.mark.integration
def test_cache_lives_inside_context_dir(sample_repo: Path) -> None:
    build_all(sample_repo, dummyindex_version="0.0.0-test")
    cache = sample_repo / ".context" / "cache"
    assert cache.exists() and cache.is_dir()
    # At least one cache entry per extracted file
    entries = list(cache.glob("*.json"))
    assert entries, "expected cache entries inside .context/cache/"


@pytest.mark.integration
def test_context_gitignore_excludes_cache(sample_repo: Path) -> None:
    build_all(sample_repo, dummyindex_version="0.0.0-test")
    gi = sample_repo / ".context" / ".gitignore"
    assert gi.exists()
    assert "cache/" in gi.read_text(encoding="utf-8")


@pytest.mark.integration
def test_cache_env_var_is_restored(sample_repo: Path) -> None:
    prior = os.environ.get("DUMMYINDEX_CACHE_DIR")
    try:
        os.environ["DUMMYINDEX_CACHE_DIR"] = "/tmp/some_prior_value"
        build_all(sample_repo, dummyindex_version="0.0.0-test")
        assert os.environ["DUMMYINDEX_CACHE_DIR"] == "/tmp/some_prior_value"
    finally:
        if prior is None:
            os.environ.pop("DUMMYINDEX_CACHE_DIR", None)
        else:
            os.environ["DUMMYINDEX_CACHE_DIR"] = prior


@pytest.mark.integration
def test_cache_env_var_unset_after_run_with_no_prior(sample_repo: Path) -> None:
    os.environ.pop("DUMMYINDEX_CACHE_DIR", None)
    build_all(sample_repo, dummyindex_version="0.0.0-test")
    assert "DUMMYINDEX_CACHE_DIR" not in os.environ


@pytest.mark.integration
def test_user_added_gitignore_lines_preserved(sample_repo: Path) -> None:
    ctx = sample_repo / ".context"
    ctx.mkdir(parents=True)
    (ctx / ".gitignore").write_text("# user-added\nlocal-notes.md\n", encoding="utf-8")
    build_all(sample_repo, dummyindex_version="0.0.0-test")
    gi_text = (ctx / ".gitignore").read_text(encoding="utf-8")
    assert "local-notes.md" in gi_text
    assert "cache/" in gi_text


# ----- managed .gitignore patterns + merge logic ----------------------------


@pytest.mark.unit
def test_fresh_gitignore_contains_every_managed_pattern(tmp_path: Path) -> None:
    ctx = tmp_path / ".context"
    ensure_context_gitignore(ctx)
    text = (ctx / ".gitignore").read_text(encoding="utf-8")
    lines = {ln.strip() for ln in text.splitlines()}
    # Exact line membership — a commented-out `# pattern` must NOT count as present.
    assert all(pattern in lines for pattern in _MANAGED_GITIGNORE_PATTERNS)


@pytest.mark.unit
def test_legacy_gitignore_upgraded_and_preserves_user_lines(tmp_path: Path) -> None:
    ctx = tmp_path / ".context"
    ctx.mkdir(parents=True)
    legacy = (
        "# Generated by dummyindex. Cache contents are local to your machine and\n"
        "# regenerate automatically — don't commit them.\n"
        "cache/\n"
        "# Pre-reorg doc snapshots (see `dummyindex context doc-reorg backup`).\n"
        "_doc_backups/\n"
        "my-local-notes/\n"
    )
    (ctx / ".gitignore").write_text(legacy, encoding="utf-8")

    ensure_context_gitignore(ctx)
    text = (ctx / ".gitignore").read_text(encoding="utf-8")
    lines = {ln.strip() for ln in text.splitlines()}
    # Every managed pattern present after the upgrade...
    assert all(pattern in lines for pattern in _MANAGED_GITIGNORE_PATTERNS)
    # ...specifically the new scratch patterns missing from the legacy body...
    for new_pattern in (
        "_enrich_plan.json",
        "_structural-plan.json",
        "_council-log.json",
        "_reality-check.json",
        "_reality-check.md",
    ):
        assert new_pattern in lines
    # ...and the user's custom line survives.
    assert "my-local-notes/" in lines

    # A second upgrade pass is a true no-op — no duplicated patterns appended.
    ensure_context_gitignore(ctx)
    again = (ctx / ".gitignore").read_text(encoding="utf-8")
    assert again == text
    stripped = [ln.strip() for ln in again.splitlines()]
    for pattern in _MANAGED_GITIGNORE_PATTERNS:
        assert stripped.count(pattern) == 1


@pytest.mark.unit
def testensure_context_gitignore_is_idempotent(tmp_path: Path) -> None:
    ctx = tmp_path / ".context"
    ensure_context_gitignore(ctx)
    first = (ctx / ".gitignore").read_text(encoding="utf-8")
    ensure_context_gitignore(ctx)
    second = (ctx / ".gitignore").read_text(encoding="utf-8")
    assert first == second
